# ── public interface ───────────────────────────────────────────────────────────


def render_team_css(team_code: str) -> str:
    """Render the complete ``extra.css`` content for a given team.

    All derived shades are computed automatically from the six canonical
    palette colours stored in :data:`~team_colors.TEAM_COLORS`.

    Args:
        team_code: Three-letter EuroLeague team code (e.g. ``"ULK"``).

    Returns:
        Fully-rendered CSS string ready to be written to disk.

    Raises:
        KeyError: If *team_code* is not found in ``TEAM_COLORS``.
    """
    palette: TeamPalette = TEAM_COLORS[team_code]

    return _CSS_TEMPLATE.format(
        team_name=palette.name or team_code,
        # canonical colours
        primary=palette.primary,
        bg_darkest=palette.bg_darkest,
        bg_header=palette.bg_header,
        bg_mid=palette.bg_mid,
        bg_light=palette.bg_light,
        text_on_primary=palette.text_on_primary,
        # derived shades
        primary_light=_lighten(palette.primary, 0.20),
        primary_dark=_darken(palette.primary, 0.20),
        primary_alpha10=_rgba(palette.primary, 0.10),
        bg_deepest=_darken(palette.bg_darkest, 0.20),
        bg_hover=_lighten(palette.bg_mid, 0.15),
    )


def write_team_css(team_code: str, output_path: str) -> None:
    """Generate and write the ``extra.css`` file for a given team.

    Args:
        team_code: Three-letter EuroLeague team code (e.g. ``"ULK"``).
        output_path: File path to write the CSS to (e.g.
            ``"docs/stylesheets/extra.css"``).

    Raises:
        KeyError: If *team_code* is not found in ``TEAM_COLORS``.
    """
    css = render_team_css(team_code)
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(css)
