"""CSS generation utilities for the MkDocs scouting-report site.

Reads a :class:`~team_colors.TeamPalette` and writes a fully-rendered
``extra.css`` file, replacing every hardcoded colour with the team's
palette.  Derived shades (lighter/darker variants, hover states, transparent
overlays) are computed programmatically so callers only need to maintain the
six canonical palette colours.

Typical usage::

    from utils_css import write_team_css
    write_team_css("ULK", "docs/stylesheets/extra.css")
"""

from team_colors import TEAM_COLORS, TeamPalette

# ── colour arithmetic helpers ──────────────────────────────────────────────────


def _hex_to_rgb(hex_color: str) -> tuple[int, int, int]:
    """Parse a ``#RRGGBB`` string to an (R, G, B) integer tuple.

    Args:
        hex_color: Hex colour string, with or without leading ``#``.

    Returns:
        Tuple of red, green, blue integers in the range [0, 255].
    """
    h = hex_color.lstrip("#")
    return int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16)


def _rgb_to_hex(r: int, g: int, b: int) -> str:
    """Format (R, G, B) integers as a ``#RRGGBB`` hex string.

    Args:
        r: Red channel [0, 255].
        g: Green channel [0, 255].
        b: Blue channel [0, 255].

    Returns:
        Uppercase hex colour string prefixed with ``#``.
    """
    return f"#{r:02X}{g:02X}{b:02X}"


def _lighten(hex_color: str, amount: float) -> str:
    """Blend a colour toward white.

    Args:
        hex_color: Source ``#RRGGBB`` colour.
        amount: Fraction in [0, 1] to blend toward white (0 = unchanged,
            1 = pure white).

    Returns:
        Lightened ``#RRGGBB`` colour string.
    """
    r, g, b = _hex_to_rgb(hex_color)
    r = min(255, int(r + (255 - r) * amount))
    g = min(255, int(g + (255 - g) * amount))
    b = min(255, int(b + (255 - b) * amount))
    return _rgb_to_hex(r, g, b)


def _darken(hex_color: str, amount: float) -> str:
    """Blend a colour toward black.

    Args:
        hex_color: Source ``#RRGGBB`` colour.
        amount: Fraction in [0, 1] to blend toward black (0 = unchanged,
            1 = pure black).

    Returns:
        Darkened ``#RRGGBB`` colour string.
    """
    r, g, b = _hex_to_rgb(hex_color)
    r = max(0, int(r * (1 - amount)))
    g = max(0, int(g * (1 - amount)))
    b = max(0, int(b * (1 - amount)))
    return _rgb_to_hex(r, g, b)


def _rgba(hex_color: str, alpha: float) -> str:
    """Format a colour as a CSS ``rgba(…)`` string.

    Args:
        hex_color: Source ``#RRGGBB`` colour.
        alpha: Opacity in [0, 1].

    Returns:
        CSS ``rgba(R, G, B, alpha)`` string.
    """
    r, g, b = _hex_to_rgb(hex_color)
    return f"rgba({r}, {g}, {b}, {alpha})"


